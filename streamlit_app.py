import streamlit as st
import pandas as pd

def main():
    # アプリ名の変更とバージョン更新
    st.set_page_config(page_title="交通事故被害者のための生活防衛ナビ v2.1.1", layout="wide")

    # --- ヘッダー・キャッチコピー ---
    st.title("🛡️ 交通事故被害者のための生活防衛ナビ v2.1.1")
    st.subheader("「黙っていたら損をする。被害者の体と生活を守る『攻め』の手順書」")
    
    st.info("""
    **【重要】交通事故も、行政と同じく「申請主義」です。** 補償は与えられるものではなく、自ら理論武装して勝ち取るものです。
    加害者側の保険会社は、必ずしも被害者の生活を第一に考えてはくれません。
    
    このアプリでは、作者が実際に事故に遭った**「2024年9月」**の失敗と、その教訓を活かした**「2026年1月」の対応**を比較し、被害者が正当な権利を守るためのステップを示します。
    """)

    # --- メインメニュー（タブ形式） ---
    tabs = st.tabs(["⚡ 初動", "🔍 検査", "🩹 治療", "⚖️ 補償・交渉"])

    # --- 1. 初動 ---
    with tabs[0]:
        st.header("ステップ1：事故直後のアクション")
        col1, col2 = st.columns(2)
        with col1:
            st.error("⚠️ **言われるがまま（2024年）**")
            st.write("""
            - 相手の「物損で」という空気に流される。
            - 軽い痛みだったので「大丈夫です」と遠慮してしまう。
            - **結果：** 警察への届け出が軽く扱われ、権利主張の土台が弱くなる。
            """)
        with col2:
            st.success("🔥 **理論武装（2026年）**")
            st.write("""
            - **即座に110番。人身事故として処理。**
            - 加害者の過失をその場で確定させる。
            - **自分の保険（自転車保険・生命保険等）の担当者に即連絡し、通院特約の有無を確認。**
            - **結果：** 警察に「人身」として記録され、正当な補償を受ける権利を確保。
            """)

    # --- 2. 検査 ---
    with tabs[1]:
        st.header("ステップ2：医療機関での検査と費用交渉")
        st.info("💡 事故から2週間経っても痛みが引かなければ、それは『別の原因』を疑うべきサインです。")
        col1, col2 = st.columns(2)
        with col1:
            st.error("⚠️ **言われるがまま（2024年）**")
            st.write("""
            - 事故当日のレントゲン検査のみ。
            - 医師の「異常なし（打撲・捻挫）」を鵜呑みにし、違和感を伏せてしまう。
            - **結果：** 原因不明の痛みを抱えたまま、早期に治療が終了。
            """)
        with col2:
            st.success("🔥 **理論武装（2026年）**")
            st.write("""
            - **「事故から一ヶ月経過してるのに痛みが引かない。レントゲンで異常なしなのはおかしい」と客観的事実を伝える。**
            - **費用交渉：** 保険会社に先に連絡し「痛みが引かないので精密検査を受けたい。医療費の直接支払いを病院へ連絡してほしい」と了承を得る。
            - 2/19にCT検査を受け、**右肋骨骨折**が判明。
            - エコー・MRI検査を経て、**右腱板損傷**が発覚。
            """)

    # --- 3. 治療 ---
    with tabs[2]:
        st.header("ステップ3：通院と休業損害")
        comparison_data = {
            "項目": ["通院日数", "休業損害（内金等）", "自身の保険からの補償", "慰謝料", "最終的な納得感"],
            "2024年の失敗": ["2日", "12,200円", "0円（未申請）", "17,200円", "金額にがっかり"],
            "2026年の戦略": ["26日以上（リハビリ開始）", "158,600円（内金獲得）", "通院補償30回分を申請", "交渉準備中", "安心して治療に専念"]
        }
        st.table(pd.DataFrame(comparison_data))
        st.write("""
        - **「通院実績＝休業損害の根拠」** と理解し、治療のための通院時間はしっかりと確保。
        - 自分の保険（自転車保険等）の「通院補償」も上限まで活用。
        """)

    # --- 4. 補償・交渉 ---
    with tabs[3]:
        st.header("ステップ4：示談交渉と専門家への相談")
        col1, col2 = st.columns(2)
        with col1:
            st.error("⚠️ **言われるがまま（2024年）**")
            st.write("- 知識がないため、増額交渉の余地や、自分の自転車保険からも補償を受けられることを知らない。")
        with col2:
            st.success("🔥 **理論武装（2026年）**")
            st.write("- **無料相談を活用し、プロのアドバイスを受ける。** 「休業損害の交渉は正当な権利」という裏付けを得て交渉。")

    st.divider()

    # --- 5. プッシュ型：生活防衛スケジュール生成 ---
    st.header("📅 被害者のための生活防衛スケジュール")
    st.write("事故日を入力してください。作者の教訓に基づいた「2週間ループ」のミッションを表示します。")

    incident_date = st.date_input("事故発生日を選択してください", value=None)

    if incident_date:
        missions = []
        
        # 0. 事故直後
        missions.append({
            "時期": "事故直後",
            "ミッション": "【初動】現場検証後、即検査。診断書を警察へ提出し人身切り替え。保険会社に医療費の直接支払いを依頼。自身の保険会社へも連絡。",
            "期限": incident_date
        })
        
        # 2週間おきのループ
        for i in range(1, 7):
            weeks = i * 2
            days = weeks * 7
            target_date = incident_date + pd.Timedelta(days=days)
            
            missions.append({
                "時期": f"{weeks}週間目",
                "ミッション": "【定期点検】痛みが引かなければ追加検査（CT/MRI）を保険会社と交渉。重症判明時は内払い交渉と弁護士無料相談を実施。",
                "期限": target_date
            })
        
        st.subheader("🛡️ 実行すべきミッション・リスト")
        st.table(pd.DataFrame(missions))

        if st.button("🚀 このスケジュールをGoogleカレンダーに登録（開発中）"):
            st.success("🔗 現在、Google Calendar APIとの連携機能を構築中です。")

if __name__ == "__main__":
    main()
